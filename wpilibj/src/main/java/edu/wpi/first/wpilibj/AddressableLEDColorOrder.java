/** Defines the order in which the LED strip displays colors. */
public enum AddressableLEDColorOrder {
  /** RGB color order. */
  kRGB,
  /** RBG color order. */
  kRBG,
  /** GBR color order. */
  kGBR,
  /** GRB color order. */
  kGRB,
  /** BRG color order. */
  kBRG,
  /** BGR color order. */
  kBGR
}
